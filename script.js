const yearNode = document.getElementById("year");
const revealNodes = document.querySelectorAll(".reveal");
const translationNodes = document.querySelectorAll("[data-i18n]");
const attributeTranslationNodes = document.querySelectorAll("[data-i18n-attr]");
const languageButtons = document.querySelectorAll("[data-language-trigger]");
const themeToggle = document.querySelector("[data-theme-toggle]");
const themeLabel = document.querySelector("[data-theme-label]");
const prefersReducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
const prefersDarkScheme = window.matchMedia("(prefers-color-scheme: dark)");
const storageKey = "site-language";
const themeStorageKey = "site-theme";

if (yearNode) {
    yearNode.textContent = new Date().getFullYear().toString();
}

const getPreferredLanguage = () => {
    const savedLanguage = window.localStorage.getItem(storageKey);

    if (savedLanguage && translations[savedLanguage]) {
        return savedLanguage;
    }

    const browserLanguage = navigator.language.toLowerCase();
    return browserLanguage.startsWith("es") ? "es" : "en";
};

const setNodeTranslation = (node, dictionary) => {
    const key = node.dataset.i18n;
    const value = dictionary[key];

    if (typeof value === "string") {
        node.textContent = value;
    }
};

const setAttributeTranslations = (node, dictionary) => {
    const mappings = node.dataset.i18nAttr.split(",");

    mappings.forEach((mapping) => {
        const [attributeName, key] = mapping.split(":").map((part) => part.trim());
        const value = dictionary[key];

        if (attributeName && typeof value === "string") {
            node.setAttribute(attributeName, value);
        }
    });
};

const setActiveLanguageButton = (language) => {
    languageButtons.forEach((button) => {
        const isActive = button.dataset.languageTrigger === language;
        button.classList.toggle("is-active", isActive);
        button.setAttribute("aria-pressed", isActive ? "true" : "false");
    });
};

const getPreferredTheme = () => {
    const savedTheme = window.localStorage.getItem(themeStorageKey);

    if (savedTheme === "light" || savedTheme === "dark") {
        return savedTheme;
    }

    return prefersDarkScheme.matches ? "dark" : "light";
};

const updateThemeToggle = (theme) => {
    if (!themeToggle || !themeLabel) {
        return;
    }

    const isDark = theme === "dark";
    themeToggle.setAttribute("aria-pressed", isDark ? "true" : "false");
    themeToggle.setAttribute("aria-label", isDark ? "Switch to light mode" : "Switch to dark mode");
    themeLabel.textContent = isDark ? "Light" : "Dark";
};

const applyTheme = (theme) => {
    document.body.dataset.theme = theme;
    document.documentElement.style.colorScheme = theme;
    window.localStorage.setItem(themeStorageKey, theme);
    updateThemeToggle(theme);
};

const applyLanguage = (language) => {
    const dictionary = translations[language] || translations.en;

    translationNodes.forEach((node) => setNodeTranslation(node, dictionary));
    attributeTranslationNodes.forEach((node) => setAttributeTranslations(node, dictionary));

    document.documentElement.lang = language;
    document.body.dataset.language = language;
    window.localStorage.setItem(storageKey, language);
    setActiveLanguageButton(language);
};

languageButtons.forEach((button) => {
    button.addEventListener("click", () => {
        applyLanguage(button.dataset.languageTrigger);
    });
});

applyLanguage(getPreferredLanguage());
applyTheme(getPreferredTheme());

if (themeToggle) {
    themeToggle.addEventListener("click", () => {
        const nextTheme = document.body.dataset.theme === "dark" ? "light" : "dark";
        applyTheme(nextTheme);
    });
}

prefersDarkScheme.addEventListener("change", (event) => {
    if (!window.localStorage.getItem(themeStorageKey)) {
        applyTheme(event.matches ? "dark" : "light");
    }
});

if (prefersReducedMotion || !("IntersectionObserver" in window)) {
    revealNodes.forEach((node) => node.classList.add("is-visible"));
} else {
    const observer = new IntersectionObserver((entries) => {
        entries.forEach((entry) => {
            if (entry.isIntersecting) {
                entry.target.classList.add("is-visible");
                observer.unobserve(entry.target);
            }
        });
    }, {
        threshold: 0.12,
        rootMargin: "0px 0px -40px 0px"
    });

    revealNodes.forEach((node) => observer.observe(node));
}
